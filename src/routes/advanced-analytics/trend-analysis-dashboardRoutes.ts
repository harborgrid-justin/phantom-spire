/**
 * Trend Analysis Dashboard API Routes
 * Handles trend analysis dashboard reporting and analytics
 */

import { Router } from 'express';
import { TrendAnalysisDashboardController } from '../../controllers/advanced-analytics/trend-analysis-dashboardController.js';
import { authenticate } from '../../middleware/auth.js';

export function createTrendAnalysisDashboardRoutes(): Router {
  const router = Router();
  const controller = new TrendAnalysisDashboardController();

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard:
   *   get:
   *     summary: Get all trend analysis dashboard entries
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: query
   *         name: status
   *         schema:
   *           type: string
   *           enum: [active, pending, completed, archived]
   *         description: Filter by status
   *       - in: query
   *         name: page
   *         schema:
   *           type: integer
   *           minimum: 1
   *           default: 1
   *         description: Page number
   *       - in: query
   *         name: limit
   *         schema:
   *           type: integer
   *           minimum: 1
   *           maximum: 100
   *           default: 20
   *         description: Number of items per page
   *     responses:
   *       200:
   *         description: Successfully retrieved trend analysis dashboard entries
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/', authenticate, controller.getAll);

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard/{id}:
   *   get:
   *     summary: Get trend analysis dashboard entry by ID
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: trend analysis dashboard entry ID
   *     responses:
   *       200:
   *         description: Successfully retrieved trend analysis dashboard entry
   *       404:
   *         description: trend analysis dashboard entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/:id', authenticate, controller.getById);

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard:
   *   post:
   *     summary: Create new trend analysis dashboard entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *             required:
   *               - title
   *               - description
   *             properties:
   *               title:
   *                 type: string
   *               description:
   *                 type: string
   *               metadata:
   *                 type: object
   *     responses:
   *       201:
   *         description: trend analysis dashboard entry created successfully
   *       400:
   *         description: Invalid input data
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.post('/', authenticate, controller.create);

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard/{id}:
   *   put:
   *     summary: Update trend analysis dashboard entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: trend analysis dashboard entry ID
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *     responses:
   *       200:
   *         description: trend analysis dashboard entry updated successfully
   *       404:
   *         description: trend analysis dashboard entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.put('/:id', authenticate, controller.update);

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard/{id}:
   *   delete:
   *     summary: Delete trend analysis dashboard entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: trend analysis dashboard entry ID
   *     responses:
   *       200:
   *         description: trend analysis dashboard entry deleted successfully
   *       404:
   *         description: trend analysis dashboard entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.delete('/:id', authenticate, controller.delete);

  /**
   * @swagger
   * /api/v1/advanced-analytics/trend-analysis-dashboard/analytics:
   *   get:
   *     summary: Get trend analysis dashboard analytics
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     responses:
   *       200:
   *         description: Successfully retrieved trend analysis dashboard analytics
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/analytics', authenticate, controller.getAnalytics);

  return router;
}
