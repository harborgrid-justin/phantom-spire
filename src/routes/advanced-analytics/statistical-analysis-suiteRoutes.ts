/**
 * Statistical Analysis Suite API Routes
 * Handles statistical analysis suite reporting and analytics
 */

import { Router } from 'express';
import { StatisticalAnalysisSuiteController } from '../../controllers/advanced-analytics/statistical-analysis-suiteController.js';
import { authenticate } from '../../middleware/auth.js';

export function createStatisticalAnalysisSuiteRoutes(): Router {
  const router = Router();
  const controller = new StatisticalAnalysisSuiteController();

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite:
   *   get:
   *     summary: Get all statistical analysis suite entries
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: query
   *         name: status
   *         schema:
   *           type: string
   *           enum: [active, pending, completed, archived]
   *         description: Filter by status
   *       - in: query
   *         name: page
   *         schema:
   *           type: integer
   *           minimum: 1
   *           default: 1
   *         description: Page number
   *       - in: query
   *         name: limit
   *         schema:
   *           type: integer
   *           minimum: 1
   *           maximum: 100
   *           default: 20
   *         description: Number of items per page
   *     responses:
   *       200:
   *         description: Successfully retrieved statistical analysis suite entries
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/', authenticate, controller.getAll);

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite/{id}:
   *   get:
   *     summary: Get statistical analysis suite entry by ID
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: statistical analysis suite entry ID
   *     responses:
   *       200:
   *         description: Successfully retrieved statistical analysis suite entry
   *       404:
   *         description: statistical analysis suite entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/:id', authenticate, controller.getById);

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite:
   *   post:
   *     summary: Create new statistical analysis suite entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *             required:
   *               - title
   *               - description
   *             properties:
   *               title:
   *                 type: string
   *               description:
   *                 type: string
   *               metadata:
   *                 type: object
   *     responses:
   *       201:
   *         description: statistical analysis suite entry created successfully
   *       400:
   *         description: Invalid input data
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.post('/', authenticate, controller.create);

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite/{id}:
   *   put:
   *     summary: Update statistical analysis suite entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: statistical analysis suite entry ID
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *     responses:
   *       200:
   *         description: statistical analysis suite entry updated successfully
   *       404:
   *         description: statistical analysis suite entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.put('/:id', authenticate, controller.update);

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite/{id}:
   *   delete:
   *     summary: Delete statistical analysis suite entry
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     parameters:
   *       - in: path
   *         name: id
   *         required: true
   *         schema:
   *           type: string
   *         description: statistical analysis suite entry ID
   *     responses:
   *       200:
   *         description: statistical analysis suite entry deleted successfully
   *       404:
   *         description: statistical analysis suite entry not found
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.delete('/:id', authenticate, controller.delete);

  /**
   * @swagger
   * /api/v1/advanced-analytics/statistical-analysis-suite/analytics:
   *   get:
   *     summary: Get statistical analysis suite analytics
   *     tags: [Advanced Analytics]
   *     security:
   *       - bearerAuth: []
   *     responses:
   *       200:
   *         description: Successfully retrieved statistical analysis suite analytics
   *       401:
   *         description: Unauthorized
   *       500:
   *         description: Internal server error
   */
  router.get('/analytics', authenticate, controller.getAnalytics);

  return router;
}
