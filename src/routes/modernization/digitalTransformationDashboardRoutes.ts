/**
 * Digital Transformation Dashboard Routes
 * Comprehensive digital transformation strategy and progress tracking
 */

import { Router } from 'express';
import { DigitalTransformationDashboardController } from '../../controllers/modernization/digitalTransformationDashboardController';
import { authMiddleware } from '../../middleware/auth';
import { validateRequest } from '../../middleware/validation';
import { rateLimitMiddleware } from '../../middleware/rateLimit';

const router = Router();
const controller = new DigitalTransformationDashboardController();

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard:
 *   get:
 *     summary: Get digital-transformation-dashboard data
 *     tags: [Modernization - digital-transformation]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: query
 *         name: limit
 *         schema:
 *           type: integer
 *         description: Number of items to return
 *       - in: query
 *         name: offset
 *         schema:
 *           type: integer
 *         description: Number of items to skip
 *       - in: query
 *         name: status
 *         schema:
 *           type: string
 *         description: Filter by status
 *     responses:
 *       200:
 *         description: Digital Transformation Dashboard data retrieved successfully
 *       401:
 *         description: Unauthorized
 *       500:
 *         description: Internal server error
 */
router.get('/', 
  authMiddleware,
  rateLimitMiddleware(100, 15), // 100 requests per 15 minutes
  controller.getDigitalTransformationDashboard
);

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard:
 *   post:
 *     summary: Create new digital-transformation-dashboard item
 *     tags: [Modernization - digital-transformation]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *               - description
 *             properties:
 *               name:
 *                 type: string
 *               description:
 *                 type: string
 *               priority:
 *                 type: string
 *                 enum: [low, medium, high, critical]
 *               category:
 *                 type: string
 *     responses:
 *       201:
 *         description: Digital Transformation Dashboard item created successfully
 *       400:
 *         description: Bad request or validation error
 *       401:
 *         description: Unauthorized
 *       500:
 *         description: Internal server error
 */
router.post('/',
  authMiddleware,
  rateLimitMiddleware(20, 15), // 20 requests per 15 minutes
  validateRequest(['name', 'description']),
  controller.createDigitalTransformationDashboardItem
);

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard/{id}:
 *   put:
 *     summary: Update digital-transformation-dashboard item
 *     tags: [Modernization - digital-transformation]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: Item ID
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               description:
 *                 type: string
 *               status:
 *                 type: string
 *                 enum: [active, pending, completed, failed, draft, in-progress]
 *               priority:
 *                 type: string
 *                 enum: [low, medium, high, critical]
 *     responses:
 *       200:
 *         description: Digital Transformation Dashboard item updated successfully
 *       400:
 *         description: Bad request or validation error
 *       401:
 *         description: Unauthorized
 *       404:
 *         description: Item not found
 *       500:
 *         description: Internal server error
 */
router.put('/:id',
  authMiddleware,
  rateLimitMiddleware(30, 15), // 30 requests per 15 minutes
  controller.updateDigitalTransformationDashboardItem
);

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard/{id}:
 *   delete:
 *     summary: Delete digital-transformation-dashboard item
 *     tags: [Modernization - digital-transformation]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: Item ID
 *     responses:
 *       200:
 *         description: Digital Transformation Dashboard item deleted successfully
 *       401:
 *         description: Unauthorized
 *       404:
 *         description: Item not found
 *       500:
 *         description: Internal server error
 */
router.delete('/:id',
  authMiddleware,
  rateLimitMiddleware(10, 15), // 10 requests per 15 minutes
  controller.deleteDigitalTransformationDashboardItem
);

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard/analytics:
 *   get:
 *     summary: Get digital-transformation-dashboard analytics
 *     tags: [Modernization - digital-transformation]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: query
 *         name: timeRange
 *         schema:
 *           type: string
 *           enum: [7d, 30d, 90d, 1y]
 *         description: Time range for analytics
 *       - in: query
 *         name: metrics
 *         schema:
 *           type: string
 *         description: Specific metrics to include
 *     responses:
 *       200:
 *         description: Digital Transformation Dashboard analytics retrieved successfully
 *       401:
 *         description: Unauthorized
 *       500:
 *         description: Internal server error
 */
router.get('/analytics',
  authMiddleware,
  rateLimitMiddleware(50, 15), // 50 requests per 15 minutes
  controller.getDigitalTransformationDashboardAnalytics
);

/**
 * @swagger
 * /api/v1/modernization/digital-transformation/digital-transformation-dashboard/health:
 *   get:
 *     summary: Health check for digital-transformation-dashboard service
 *     tags: [Modernization - digital-transformation]
 *     responses:
 *       200:
 *         description: Service is healthy
 *       503:
 *         description: Service is unhealthy
 */
router.get('/health',
  rateLimitMiddleware(200, 15), // 200 requests per 15 minutes
  controller.healthCheck
);

export default router;